// Helper function to get total seats based on coach type
const getTotalSeats = (coachType: string): number => {
  switch (coachType) {
    case "minibus":
      return 16;
    case "limousine":
      return 45;
    case "limousine_cabin":
      return 24;
    default:
      return 20;
  }
};

